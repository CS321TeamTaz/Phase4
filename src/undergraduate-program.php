      <h1 style="text-align:center;">Undergraduate Program</h1>
      <img id="text-image" src="../img/marines.jpg">
      <p> The study of history begins with questions about the past; how things came to be as they are or were. These questions can
        contribute to a greater understanding of ourselves, others and the world in which we live.</p>
      <p> Historians approach the study of the past in many ways. Some attempt to analyze the spectrum of historical evolution within
        a particular period or within a specific nation. Others, working within or across national histories, focus on the trajectory
        of particular social institutions, such as the family, government, business or religion. Alternately, historians can
        investigate the historical development of ideologies or of cultural concepts such as race or gender. Historians borrow
        investigative strategies freely from other disciplines. For some historians, the methodologies of the social sciences
        become critical tools for the study of the past, while others prefer a historical approach more akin to the methods of
        the humanities and literature. Most adopt some mixture of methodologies.</p>
      <p> In the College of Arts and Sciences at SIUE, the Department of Historical Studies emphasizes the study of primary sources
        beginning with our freshman-level survey labs, through the senior research projects. We also provide numerous opportunities
        for experiential learning through internships at many venues in the greater St. Louis region and in the Metro East. A
        concentration in applied historical methods is also available for those interested in editing and digital technology.
        The social science secondary education minor for licensure in teaching is housed in the Department of Historical Studies.</p>
    
