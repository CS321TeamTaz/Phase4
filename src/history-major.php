        <h1 style="text-align:center;">Historical Studies Major</h1>
        <img id="text-image" src="../img/cover.jpg">

       <p> The Department of Historical Studies has two options within its bachelor’s degree program. One, the Bachelor of Arts (BA) degree,
         is often the first step in preparation for a career as a professional historian. It is also excellent preparation for law school
          or other professional training. The Bachelor of Science (BS) degree may be preferred by students contemplating careers in the
          business world, government service, journalism, and editing. Students pursuing either a BA or a BS degree may seek careers in museums,
          archives, national parks, monuments, or other institutions where historical analysis is required. To prepare students for this sort
          of work, the department offers HIST 490, an internship with an historical agency. Finally, students planning to teach in public schools
          may choose either a bachelor of arts or a bachelor of science degree with a major in history. Any of these programs provides an opportunity
          for students to study subjects of great interest while developing skills that prepare them for a variety of career options.</p>
        
       <p>The bachelor of science program is identical to the bachelor of arts program, except bachelor of science students are not required
             to study a foreign language. A foreign language is strongly recommended for students planning graduate study.</p>

       <p>Requirements for history majors:</p>
             
       <ul>
          <li>Must pass ENG 102</li>
          <li>Must maintain a 2.0 grade point average</li>
          <li>Must maintain a 2.0 grade point average in all Historical Studies courses</li>
          <li>For transfer students 
          <ul>
          <li>Must have a 2.0 grade point average</li>
          <li>Courses are accepted pending similarity to Historical Studies offerings and articulation agreements with the student’s prior institution</li>
          </ul>
          </li>
          <li>Complete all general education and specific program requirements</li>
          <li style="list-style: none;">Complete four courses of HIST survey courses, 101-201 with a minimum grade of C 
          <ul>
          <li>Two must be from the European or world surveys (111A/B, 112A/B)</li>
          <li>Two must be from the United States Surveys (130, 200, 201)</li>
          <li>Students preparing for teacher certification to teach history or social science must select HIST 112A/B</li>
          </ul>
          </li>
          <li>Complete six courses of upper level courses 300-499 with a minimum grade of C. At least two of these six courses must be at the 400-level (400-499). Students preparing for certification to teach history or social studies must select History/Pedagogy, HIST 323</li>
          <li>Complete HIST 301 &amp; HIST 401 with a minimum grade of C.</li>
          <li>Social Science Education minors must average 3.0 cumulatively in their Historical Studies courses.</li>
          </ul>

          <p>Graduation Requirements</p>

          <ul>
              <li>Complete all specific program requirements.</li>
              <li>Students are required to complete a minor.</li>
              <li>Complete all University requirements including: All general education requirements, a minimum of 124 credit hours, at least 30 of which must be completed at SIUE; At least 60 of which must be completed at a regionally accredited 4-year institution; A minimum cumulative grade point average of 2.0</li>
              <li>Bachelor of Arts only: one year of the same foreign language and 6 courses in fine and performing arts or humanities</li>
              <li>File an Application for Graduation by the first day of the term in which you plan to graduate.</li>
              </ul>
